--Query 7
SELECT customer_id, COUNT(*) AS order_count
FROM orders
GROUP BY customer_id
HAVING COUNT(*) > 1
--Mistake explanation
--HAVING was placed before GROUP BY. SQL requires GROUP BY first, followed by HAVING.
--Business meaning
--Shows customers who have placed more than one order.


