
--Query 1
SELECT customers.city, COUNT(*) AS order_count
FROM orders INNER JOIN customers ON orders.customer_id = customers.customer_id
GROUP BY city;
--Mistake explanation
--The orders table does not contain a city column. The query must join the customers table to access the customer's city.
--Business meaning
--Shows how many orders were placed from each city.

--Query 2
SELECT product_name, SUM(quantity * price) AS revenue
FROM orders
JOIN products ON orders.product_id = products.product_id
WHERE status = 'completed'
GROUP BY product_name;
--Mistake explanation
--A comma was missing after product_name. SQL requires commas to separate selected columns.
--Business meaning
--Shows the completed revenue generated by each product.


--Query 3
SELECT status, COUNT(*) AS order_count
FROM orders
GROUP BY status
ORDER BY order_count DESC;
--Mistake explanation
--A semicolon was placed after GROUP BY status, which ended the statement before ORDER BY. ORDER BY must be part of the same query.
--Business meaning
--Shows the number of orders for each status, sorted from highest to lowest.


--Query 4
SELECT order_id, quantity, price, quantity * price AS total_amount FROM orders
INNER JOIN products ON orders.product_id = products.product_id
--Mistake explanation
--The price column does not exist in the orders table. It must be retrieved from the products table by joining on product_id.
--Business meaning
--Calculates the total amount for every order.


--Query 5
SELECT category, SUM(quantity) AS total_quantity
FROM orders
INNER JOIN products ON orders.product_id = orders.product_id
WHERE status = 'completed'
GROUP BY category;
--Mistake explanation
--This compares the same column to itself, creating an incorrect join. It should join orders.product_id with products.product_id.
--Business meaning
--Shows the total quantity sold for each product category using only completed orders.


--Query 6
